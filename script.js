// Prompt user for the first input
var firstInput = prompt("Enter the first number:");

// Multiply the first input by 2
var result = parseInt(firstInput) * 2;

// Prompt user for the second input
var secondInput = prompt("Enter the second number:");

// Add the second input to the result
result += parseInt(secondInput);

// Display the final result using alert
alert("The final result is: " + result);

